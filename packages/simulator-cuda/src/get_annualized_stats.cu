FLOAT _get_empirical_annual_non_log_mean_from_indexes(
    const thrust::device_vector<uint32_t> &sampled_indexes,
    const thrust::device_vector<FLOAT> &historical_monthly_log_returns) {

  const thrust::device_vector<FLOAT> monthly_log_series =
      _apply_sample(sampled_indexes, historical_monthly_log_returns);
  const thrust::device_vector<FLOAT> annual_log_returns =
      annualize_series(monthly_log_series);

  return thrust::transform_reduce(
             annual_log_returns.begin(),
             annual_log_returns.end(),
             [] __device__(const FLOAT x) -> FLOAT { return FLOAT_EXPM1(x); },
             FLOAT_L(0.0),
             thrust::plus<FLOAT>()) /
         static_cast<FLOAT>(annual_log_returns.size());
}

StocksAndBonds<FLOAT> get_empirical_annual_non_log_mean(
    const uint64_t seed,
    const uint32_t num_runs,
    const uint32_t num_months,
    const thrust::device_vector<FLOAT> &historical_monthly_log_returns_stocks,
    const thrust::device_vector<FLOAT> &historical_monthly_log_returns_bonds) {

  assert(historical_monthly_log_returns_stocks.size() ==
         historical_monthly_log_returns_bonds.size());

  const thrust::device_vector<uint32_t> sampled_indexes = sample(
      seed, num_runs, num_months, historical_monthly_log_returns_stocks.size());

  return StocksAndBonds<FLOAT>{
      .stocks = _get_empirical_annual_non_log_mean_from_indexes(
          sampled_indexes, historical_monthly_log_returns_stocks),
      .bonds = _get_empirical_annual_non_log_mean_from_indexes(
          sampled_indexes, historical_monthly_log_returns_bonds)};
}

TEST_CASE("STAR::get_empirical_annual_non_log_mean") {
  const StocksAndBonds<FLOAT> result = get_empirical_annual_non_log_mean(
      0,
      100'000,
      50 * 12,
      _get_historical_monthly_log_returns_stocks_v1(),
      _get_historical_monthly_log_returns_bonds_v1());
  CHECK(result.stocks - FLOAT_L(0.0807135999) < 1e-4);
  CHECK(result.bonds - FLOAT_L(0.0289038382) < 1e-4);
}

